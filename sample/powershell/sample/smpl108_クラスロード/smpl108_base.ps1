#==============================================================================
# テストクラス
#==============================================================================
class CTestBase {
    # メンバ
    $msg

    #============================================================
    # コンストラクタ
    #------------------------------------------------------------
    # 引数 : なし
    #============================================================
    CTestBase(){
        $this.msg = "TestBase"
    }

    #============================================================
    # セットメッセージ
    #------------------------------------------------------------
    # 引数   : なし
    # 戻り値 : なし
    #============================================================
    [void] SetMsg(){
        $this.msg = "TestBase"
    }

    #============================================================
    # メッセージ表示
    #------------------------------------------------------------
    # 引数   : なし
    # 戻り値 : なし
    #============================================================
    [void] Dispmsg(){
        $this.SetMsg()
        $this.DispmsgSub2()
    }

    #============================================================
    # メッセージ表示サブ１
    #------------------------------------------------------------
    # 引数   : なし
    # 戻り値 : なし
    #============================================================
    [void] DispmsgSub1(){
        $this.SetMsg()
        Write-Host ($this.msg + "1")
    }

    #============================================================
    # メッセージ表示サブ２
    #------------------------------------------------------------
    # 引数   : なし
    # 戻り値 : なし
    #============================================================
    [void] DispmsgSub2(){
        $this.SetMsg()
        Write-Host ($this.msg + "2")
    }

}
