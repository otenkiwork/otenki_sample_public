#==============================================================================
# ファイルアクセスクラス
#==============================================================================
class CComFileAccess {
    # メンバ
    $filePath = ""
    $fileData = ""

    #============================================================
    # コンストラクタ
    #------------------------------------------------------------
    # 引数 : なし
    #============================================================
    CComFileAccess(){
        $this.filePath = ""
        $this.fileData = ""
    }

    #============================================================
    # ファイル読み込み
    #------------------------------------------------------------
    # 引数   : $pFilePath : ファイルパス
    # 戻り値 : なし
    #============================================================
    [void] ReadFile($pFilePath){
        $this.filePath = $pFilePath
        $this.fileData = Get-Content $this.filePath
    }

    #============================================================
    # データリスト取得
    #------------------------------------------------------------
    # 引数   : なし
    # 戻り値 : データリスト
    #============================================================
    [object[]] GetData(){
        return $this.fileData
    }
}
